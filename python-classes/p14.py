#Base Conversion 
# int,float,str, bool, NoneType....
# number system changes functions : bin(), oct(), hex()

no = int(input('Enter the No:'))
print('no is :',no)
print('type of no:',type(no))

#base convert in some other base 
print(f'Binary of {no} is {bin(no)}')
print(f'Octal of {no} is {oct(no)}')
print(f'Hexadecimal of {no} is {hex(no)}')





