# for loop in python 
# use of for loop are normal loop then we use range 
# we can maintain external counter with 
# for loop but it is not recommended because for loop is used when we know the number of iterations


counter = 1
for i in range(1,10):
    counter = counter+1
    print(f'value of i = {i}')
    print(f'counter = {counter}')
