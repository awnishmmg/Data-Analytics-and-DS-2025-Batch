# wap in python to find the area of a circle

def area_of_circle(radius):
    '''
    This function calculates the area of a circle given its radius.
    @author: Awnish Kumar
    @date: 2024-06-01
    @time: 16:00
    @param radius: The radius of the circle
    @return: The area of the circle
    @example: area_of_circle(5) -> 78.53975
    '''
    pi = 3.14159
    area = pi * (radius ** 2)
    return area
