import math

no = 3
print(f' square of {no} = {no*no}')
print(f' cube of {no} = {no*no*no}')
print(f' 5th power of {no} = {no*no*no*no*no}')
print(f' 5th power of {no} = {no**5}') # if exponent or power not available then we need math.

print(f' 5th power of {no} = {math.pow(no,5)}')
print(f' 2nd power of {no} = {math.pow(no,3)}')
